
public class Ex01_Operator {
	public static void main(String[] args) {
		
		int num1 = 2;
		int num2 = 3;
		System.out.println("num1 + num2 = " + (num1 + num2));
		System.out.println("num1 - num2 = " + (num1 - num2));
		System.out.println("num1 * num2 = " + (num1 * num2));
		System.out.println("num1 / num2 = " + (num1 / num2));
		System.out.println("2로 나누고 나머지가 0이면 짝수" + (num1 % 2));
		System.out.println("2로 나누고 나머지가 1이면 홀수" + (num2 % 2));
	}
}
