package pack01.exif;

public class Test03_If {
	public static void main(String[] args) {
		char char1 = 'O';
		if (char1 == 'O') {
			System.out.println("O형입니다.");
		} else if (char1 == 'A') {
			System.out.println("A형 입니다.");
		} else if (char1 == 'B') {
			System.out.println("B형 입니다.");
		} else {
			System.out.println("잘못된 입력!");
		}
	}
}
