package pack01.exif;

public class Test02_Ifelse2 {
	public static void main(String[] args) {
		int num = 10;
		if (num %2 ==0) {
			System.out.println("짝수");
		} else {
			System.out.println("홀수");
		}
		if (num %3 ==0) {
			System.out.println("3의 배수입니다.");
		} else {
			System.out.println("3의 배수가 아닙니다.");
		}
	}
}
