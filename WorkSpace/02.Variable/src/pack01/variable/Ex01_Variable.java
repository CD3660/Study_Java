package pack01.variable;

public class Ex01_Variable {
	public static void main(String[] args) {
		int x;
		int y;
		int z;
		x = 5;
		y = 10;
		z = 15;		
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		System.out.println(x + "" + y + "" + z);
		System.out.print(x);
		System.out.print(y);
		System.out.println(z);
	}
}
